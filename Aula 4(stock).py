p1 = input("Diz o nome do teu produto 1.")
qp1 = int(input("Diz a quantidade de produto 1 que tens em stock."))
pr1 = int(input("Diz o preço unitário do teu produto 1."))
vt1 =(qp1*pr1)
p2 = input("Diz o nome do teu produto 2.")
qp2 = int(input("Diz a quantidade de produto 2 que tens em stock."))
pr2 = int(input("Diz o preço unitário do teu produto 2."))
vt2 =(qp2*pr2)
