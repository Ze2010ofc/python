qp3 = int(input("Diz a quantidade de produto 3 que tens em stock."))
pr3 = int(input("Diz o preço unitário do teu produto 3."))
vt3 =(qp3*pr3)
tt =(vt3+vt1+vt2)
print(f'"Tu tens em stock, {vt1}€ em {p1}, {vt2}€ em {p2} e {vt3}€ em {p3}."')
print(f'"O valor total de todos os teus produtos é de {tt}€."')
